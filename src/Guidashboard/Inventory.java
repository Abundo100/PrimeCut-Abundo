       private void filterTable(String query) {
   // Get whatever model is currently inside the table (from DbUtils)
    javax.swing.table.TableModel model = tblInventory.getModel();
    TableRowSorter<javax.swing.table.TableModel> sorter = new TableRowSorter<>(model);
    tblInventory.setRowSorter(sorter);

    if (query.trim().length() == 0 || query.equals("Search")) {
        sorter.setRowFilter(null);
    } else {
        // (?i) makes the search ignore uppercase/lowercase differences
        sorter.setRowFilter(RowFilter.regexFilter("(?i)" + query));
    }
}


