private void generateReceipt(String name, String qty, String price, String total) {
    try {
       String path = System.getProperty("user.home") + "/Desktop/Receipt_" + System.currentTimeMillis() + ".pdf";
        Document doc = new Document();
        PdfWriter.getInstance(doc, new FileOutputStream(path));
        doc.open();
        doc.add(new Paragraph("********** PRIMECUT MEAT SHOP **********"));
        doc.add(new Paragraph("Customer Receipt"));
        doc.add(new Paragraph("Date: " + new java.util.Date().toString()));
        doc.add(new Paragraph("----------------------------------------"));
        doc.add(new Paragraph("Item: " + name));
        doc.add(new Paragraph("Quantity: " + qty + " kg"));
        doc.add(new Paragraph("Unit Price: PHP " + price));
        doc.add(new Paragraph("----------------------------------------"));
        doc.add(new Paragraph("TOTAL AMOUNT: PHP " + total));
        doc.add(new Paragraph("----------------------------------------"));
        doc.add(new Paragraph("Thank you for your purchase!"));
        doc.close();
        
        File file = new File(path);
        if (file.exists()) {
            Desktop.getDesktop().open(file);
        }
    } catch (Exception e) {
        javax.swing.JOptionPane.showMessageDialog(this, "PDF Error: " + e.getMessage());
    }
}


