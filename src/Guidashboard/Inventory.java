private void calculateTotal() {
    try {
        double price = Double.parseDouble(txtTransPrice.getText());
        double qty   = Double.parseDouble(txtTransQty.getText());
        double total = price * qty;
        lblTotalDisplay.setText(String.format("%.2f", total));
    } catch (NumberFormatException e) {
        lblTotalDisplay.setText("");
    }
}
private void sellProduct() {
    String name  = txtTransName.getText();
    String price = txtTransPrice.getText();
    String qty   = txtTransQty.getText();
    String total = lblTotalDisplay.getText();

    // ✅ Keep your original empty check
    if (name.isEmpty() || price.isEmpty() || qty.isEmpty()) {
        javax.swing.JOptionPane.showMessageDialog(this, "Please fill in all fields!");
        return;
    }

    // ✅ ADD THIS: Stock check before showing confirm dialog
    try {
        config.db_pcut dbCheck = new config.db_pcut();
        java.sql.ResultSet rs = dbCheck.getData(
            "SELECT p_stock, p_status FROM products WHERE p_name = '" + name + "'"
        );
        if (rs.next()) {
            double currentStock = rs.getDouble("p_stock");
            String status = rs.getString("p_status");
            double requestedQty = Double.parseDouble(qty);

            if (status.equalsIgnoreCase("Not Available") || currentStock <= 0) {
                javax.swing.JOptionPane.showMessageDialog(this,
                    "Sorry, " + name + " is currently OUT OF STOCK!",
                    "Out of Stock", javax.swing.JOptionPane.WARNING_MESSAGE);
                return;
            }

            if (requestedQty > currentStock) {
                javax.swing.JOptionPane.showMessageDialog(this,
                    "Not enough stock! Only " + currentStock + " kg available.",
                    "Insufficient Stock", javax.swing.JOptionPane.WARNING_MESSAGE);
                return;
            }
        }
    } catch (Exception e) {
        javax.swing.JOptionPane.showMessageDialog(this, "Stock check error: " + e.getMessage());
        return;
    }

    // ✅ Your original confirm dialog — NOT changed
    int confirm = javax.swing.JOptionPane.showConfirmDialog(this,
        "Confirm sale of " + qty + "kg of " + name + " for PHP " + total + "?",
        "Confirm Sale", javax.swing.JOptionPane.YES_NO_OPTION);

    if (confirm == javax.swing.JOptionPane.YES_OPTION) {
        try {
            config.db_pcut db = new config.db_pcut();

            // ✅ Your original — NOT changed
            String userName = config.Session.getFname() + " " + config.Session.getLname();

            // ✅ Your original — NOT changed
            String histSql = "INSERT INTO transaction_history (product_name, quantity, total_price, date_sold, user_name) VALUES (?, ?, ?, datetime('now'), ?)";
            db.addRecord(histSql, name, qty, total, userName);

            // ✅ CHANGED: Now also auto-updates status to "Not Available" when stock hits 0
            String updateSql = "UPDATE products SET p_stock = p_stock - ?, " +
                "p_status = CASE WHEN (p_stock - ?) <= 0 THEN 'Not Available' ELSE p_status END " +
                "WHERE p_name = ?";
            db.updateRecord(updateSql, qty, qty, name);

            // ✅ Your original — NOT changed
            txtTransName.setText("");
            txtTransPrice.setText("");
            txtTransQty.setText("");
            lblTotalDisplay.setText("");

            // ✅ Your original — NOT changed
            displayTable();
            javax.swing.JOptionPane.showMessageDialog(this, "Sale recorded successfully!");
            generateReceipt(name, qty, price, total);

        } catch (Exception e) {
            javax.swing.JOptionPane.showMessageDialog(this, "Error: " + e.getMessage());
        
    
        }
    }
}
private void autoFillPrice() {
    try {
        String productName = txtTransName.getText();
        if (productName.isEmpty()) {
            txtTransPrice.setText("");
            lblTotalDisplay.setText("");
            return;
        }

        config.db_pcut db = new config.db_pcut();
        java.sql.ResultSet rs = db.getData(
            "SELECT p_price FROM products WHERE p_name = '" + productName + "'"
        );

        if (rs.next()) {
            String price = rs.getString("p_price");
            txtTransPrice.setText(price);
            calculateTotal(); // Auto calculate total too
        } else {
            txtTransPrice.setText("");
            lblTotalDisplay.setText("");
        }
    } catch (Exception e) {
        System.out.println("AutoFill Error: " + e.getMessage());
    }
}
